    public void loadData() {
        Object[] columnNames = {"STT", "Mã Đơn Vị", "Tên Đơn Vị", ""};
//        if (nameSeaechInput != null) {
//            dfmThietBi = thiet_biService.search(nameSeaechInput);
//        } else {
//            dfmThietBi = cauhinhDAO.getAll();
//        }
        list_don_vi = don_viService.findAll();

        dfmDonVi = new DefaultTableModel(new Object[0][0], columnNames);
        int index = 1;
        for (don_viModel adv : list_don_vi) {
            Object[] o = new Object[4];
            o[0] = index;
            o[1] = adv.getId();
            o[2] = adv.getName_DV();
            dfmDonVi.addRow(o);
            index++;
        }
        tblDonVi.setModel(dfmDonVi);
        funBase.addCheckBox(3, tblDonVi);

        // Cài đặt sự kiện khi click từng dòng trong bảng
        if (list_don_vi.size() > 0) {
            tblDonVi.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    int currentRow = tblDonVi.getSelectedRow();
                    if (currentRow < 0) {
                        currentRow = 0;
                    }
                    // Chèn dữ liệu lên form
                    if (currentRow >= 0) {
                        txt_idDV.setText(tblDonVi.getValueAt(currentRow, 1).toString());
                        txtTendonvi.setText(tblDonVi.getValueAt(currentRow, 2).toString());
                    }
                }
            });
        }
    }
