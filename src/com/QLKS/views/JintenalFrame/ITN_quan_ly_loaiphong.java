    public void loadData() {
        Object[] columnNames = {"STT", "Mã Loại Phòng", "Tên loại phòng", "Giá", "Số Người Tối Đa", ""};
//        if (nameSeaechInput != null) {
//            dfmThietBi = thiet_biService.search(nameSeaechInput);
//        } else {
//            dfmThietBi = cauhinhDAO.getAll();
//        }
        list_LP = loaiService.findAll();
        
        dfmloaiPhong = new DefaultTableModel(new Object[0][0], columnNames);
        int index = 1;
        for (loai_phongModel adv : list_LP) {
            Object[] o = new Object[6];
            o[0] = index;
            o[1] = adv.getId();
            o[2] = adv.getName_LP();
            o[3] = adv.getPrice();
            o[4] = adv.getMaxPeople();
            dfmloaiPhong.addRow(o);
            index++;
        }
        tblLoaiPhong.setModel(dfmloaiPhong);
        funBase.addCheckBox(5, tblLoaiPhong);

        // Cài đặt sự kiện khi click từng dòng trong bảng
        if (list_LP.size() > 0) {
            tblLoaiPhong.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    int currentRow = tblLoaiPhong.getSelectedRow();
                    if (currentRow < 0) {
                        currentRow = 0;
                    }
                    // Chèn dữ liệu lên form
                    if (currentRow >= 0) {
                        txtMaLoaiPhong.setText(tblLoaiPhong.getValueAt(currentRow, 1).toString());
                        txtTenLoaiPhong.setText(tblLoaiPhong.getValueAt(currentRow, 2).toString());
                        txtGiaLoaiPhong.setText(tblLoaiPhong.getValueAt(currentRow, 3).toString());
                        txtSoNguoiToiDa.setText(tblLoaiPhong.getValueAt(currentRow, 4).toString());
                        txtMaLoaiPhong.setEditable(false);
                    }
                }
            });
        }
    }

