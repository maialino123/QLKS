    private void button_addDVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_addDVMouseClicked
        Float giadich_vu = Float.parseFloat(txtGiaDichVu.getText());
        loai_dich_vuModel loaiDV = (loai_dich_vuModel) modelLoai_dich_vu.getSelectedItem();
        Long id_LDVM = loaiDV.getId();
        don_viModel don_viCB = (don_viModel) modelDon_vi.getSelectedItem();
        Long id_DonVi = don_viCB.getId();
        boolean check_DV = true;
        if (giadich_vu == 0) {
            txtErrorTenLoaiDichVu1.setText("Giá Không được để trốnng!");
            check_DV = false;
        } else {
            txtErrorTenLoaiDichVu1.setText("");
        }
        
        if (check_DV == true) {
            dichModel.setId_LDV(id_LDVM);
            dichModel.setId_DV(id_DonVi);
            dichModel.setPrice(giadich_vu);
            Long key = dich_vuService.add(dichModel);
            if (key > 0) {
                JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công!");
                ressetText();
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm Mới Thất Bại!");
            }
        }
    }//GEN-LAST:event_button_addDVMouseClicked

