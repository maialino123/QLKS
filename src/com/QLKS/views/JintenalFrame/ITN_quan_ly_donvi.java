    private void button_updateDvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_updateDvMouseClicked
        Long id_don_vi = Long.parseLong(txt_idDV.getText());
        String name_don_vi = txtTendonvi.getText();
        boolean check_Don_Vi = true;
        if (name_don_vi.length() == 0) {
            txtErrorTendonvi.setText("Tên Đơn Vị Không Được Để Trống!");
            check_Don_Vi = false;
        } else {
            txtTendonvi.setText("");
        }

        if (check_Don_Vi == true) {
            don_viModel.setId(id_don_vi);
            don_viModel.setName_DV(name_don_vi);
            int key = don_viService.edit(don_viModel);
            if (key > 0) {
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công!");
                ressetText();
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Cập Nhật Thất Bại!");
            }
        }
    }//GEN-LAST:event_button_updateDvMouseClicked

    private void delete_don_viMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delete_don_viMouseClicked
       String succesDeltete = "";
        String errDeltete = "";
        boolean check_del = false;

        int thongBao = JOptionPane.showConfirmDialog(this, "Bạn có chắc không?", "Thông báo!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (thongBao == JOptionPane.YES_OPTION) {

            for (int i = 0; i < tblDonVi.getRowCount(); i++) {
                if (funBase.IsSelected(i, 3, tblDonVi)) {
                    check_del = true;
                    int rowSuccess = don_viService.delete(Long.parseLong(tblDonVi.getValueAt(i, 1).toString()));
                    tblDonVi.clearSelection();
                    if (rowSuccess > 0) {
                        succesDeltete += "\t" + tblDonVi.getValueAt(i, 2).toString() + "\n";
                    } else {
                        errDeltete += "\t" + tblDonVi.getValueAt(i, 2).toString() + "\n";
                    }
                }
            }
            ressetText();
            loadData();
            if (check_del == true) {
                String mess = "";
                if (succesDeltete.length() > 0) {
                    mess += "Bạn đã xóa thành công: \n" + succesDeltete;

                }
                if (errDeltete.length() > 0) {
                    mess += "Không thể xóa: \n" + errDeltete;
                }
                JOptionPane.showMessageDialog(rootPane, mess, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn hàng để xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_delete_don_viMouseClicked


