    public void loadData() {
        Object[] columnNames = {"STT", "Mã Loại Dịch Vụ", "Tên loại dịch vụ", ""};
//        if (nameSeaechInput != null) {
//            dfmThietBi = thiet_biService.search(nameSeaechInput);
//        } else {
//            dfmThietBi = cauhinhDAO.getAll();
//        }
        list_TTHD = trang_thaiService.findAll();

        dfmTrangThaiHoaDon = new DefaultTableModel(new Object[0][0], columnNames);
        int index = 1;
        for (trang_thai_hoa_donModel adv : list_TTHD) {
            Object[] o = new Object[4];
            o[0] = index;
            o[1] = adv.getId();
            o[2] = adv.getName();
            dfmTrangThaiHoaDon.addRow(o);
            index++;
        }
        tblTTHD.setModel(dfmTrangThaiHoaDon);
        funBase.addCheckBox(3, tblTTHD);

        // Cài đặt sự kiện khi click từng dòng trong bảng
        if (list_TTHD.size() > 0) {
            tblTTHD.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    int currentRow = tblTTHD.getSelectedRow();
                    if (currentRow < 0) {
                        currentRow = 0;
                    }
                    // Chèn dữ liệu lên form
                    if (currentRow >= 0) {
                        lblID.setText(tblTTHD.getValueAt(currentRow, 1).toString());
                        txtTenTrangThaiHD.setText(tblTTHD.getValueAt(currentRow, 2).toString());
                    }
                }
            });
        }
    }
