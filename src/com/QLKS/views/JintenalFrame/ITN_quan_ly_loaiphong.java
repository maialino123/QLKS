    private void button_add_LPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_add_LPMouseClicked
        String nameLP = txtTenLoaiPhong.getText();
        Float giaLP = Float.parseFloat(txtGiaLoaiPhong.getText());
        int soNguoiTD = Integer.parseInt(txtSoNguoiToiDa.getText());
        loai_phongModel getNameLP = loaiService.findByName(nameLP);
        if (getNameLP == null) {
            loai_phongModel.setName_LP(nameLP);
            loai_phongModel.setPrice(giaLP);
            loai_phongModel.setMaxPeople(soNguoiTD);
            Long key = loaiService.add(loai_phongModel);
            if (key >= 0) {
                JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công!");
                ressetText();
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm Mới Thất Bại!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Loại phòng này đã tồn tại!");
        }
    }//GEN-LAST:event_button_add_LPMouseClicked

