    private void button_add_LDVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_add_LDVMouseClicked
        String name_LDV = txtTenLoaiDichVu.getText();
        boolean check_err = true;
        if (name_LDV.length() == 0) {
            txtErrorTenLoaiDichVu.setText("Loại dịch vụ không được để trống!");
            check_err = false;
        } else {
            txtErrorTenLoaiDichVu.setText("");
        }

        if (check_err == true) {
            loai_dich_vuModel getNameModel = loai_dich_vuService.findByName(name_LDV);
            if (getNameModel == null) {
                loai_dich_vuModel.setName(name_LDV);
                Long keys = loai_dich_vuService.add(loai_dich_vuModel);
                if (keys >= 0) {
                    JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công!");
                    ressetText();
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm Mới Thất Bại!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Loại dịch vụ đã tồn tại");
            }
        }
    }//GEN-LAST:event_button_add_LDVMouseClicked

    private void button_updateLDVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_updateLDVMouseClicked
        String name_LDV = txtTenLoaiDichVu.getText();
        Long id_LDV = Long.parseLong(txtMaLoaiDichVu.getText());
        boolean check_err = true;
        if (name_LDV.length() == 0) {
            txtErrorTenLoaiDichVu.setText("Loại dịch vụ không được để trống!");
            check_err = false;
        } else {
            txtErrorTenLoaiDichVu.setText("");
        }

        if (check_err == true) {
            loai_dich_vuModel.setId(id_LDV);
            loai_dich_vuModel.setName(name_LDV);
            int key = loai_dich_vuService.edit(loai_dich_vuModel);
            if (key >= 0) {
                JOptionPane.showMessageDialog(this, "Cập nhật Thành Công!");
                ressetText();
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật Thất Bại!");
            }
        }
    }//GEN-LAST:event_button_updateLDVMouseClicked

    private void button_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_deleteMouseClicked
        String succesDeltete = "";
        String errDeltete = "";
        boolean check_del = false;

        int thongBao = JOptionPane.showConfirmDialog(this, "Bạn có chắc không?", "Thông báo!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (thongBao == JOptionPane.YES_OPTION) {

            for (int i = 0; i < tblLoaiDichVu.getRowCount(); i++) {
                if (funBase.IsSelected(i, 3, tblLoaiDichVu)) {
                    check_del = true;
                    int rowSuccess = loai_dich_vuService.delete(Long.parseLong(tblLoaiDichVu.getValueAt(i, 1).toString()));
                    tblLoaiDichVu.clearSelection();
                    if (rowSuccess > 0) {
                        succesDeltete += "\t" + tblLoaiDichVu.getValueAt(i, 2).toString() + "\n";
                    } else {
                        errDeltete += "\t" + tblLoaiDichVu.getValueAt(i, 2).toString() + "\n";
                    }
                }
            }
            ressetText();
            loadData();
            if (check_del == true) {
                String mess = "";
                if (succesDeltete.length() > 0) {
                    mess += "Bạn đã xóa thành công: \n" + succesDeltete;

                }
                if (errDeltete.length() > 0) {
                    mess += "Không thể xóa: \n" + errDeltete;
                }
                JOptionPane.showMessageDialog(rootPane, mess, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn hàng để xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_button_deleteMouseClicked

