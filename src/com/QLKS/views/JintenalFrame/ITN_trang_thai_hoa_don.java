    private void button_addTTHDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_addTTHDMouseClicked
        String name = txtTenTrangThaiHD.getText();
        boolean check_TTHD = true;
        if (name.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống!");
            check_TTHD = false;
        }
        if (check_TTHD == true) {
            trang_thai_hoa_donModel getNameTT = trang_thaiService.findByName(name);
            if (getNameTT == null) {
                trang_thaiModel.setName(name);
                Long key = trang_thaiService.add_TTHD(trang_thaiModel);
                if (key > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công!");
                    ressetText();
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm Mới Thất Bại!");
                    loadData();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Đã tồn tại!");
            }
        }
    }//GEN-LAST:event_button_addTTHDMouseClicked

