    public void loadData() {
        Object[] columnNames = {"STT", "Mã Loại Dịch Vụ", "Tên loại dịch vụ", ""};
//        if (nameSeaechInput != null) {
//            dfmThietBi = thiet_biService.search(nameSeaechInput);
//        } else {
//            dfmThietBi = cauhinhDAO.getAll();
//        }
        list_LDV = loai_dich_vuService.findAll();

        dfmLoaiDichVu = new DefaultTableModel(new Object[0][0], columnNames);
        int index = 1;
        for (loai_dich_vuModel adv : list_LDV) {
            Object[] o = new Object[4];
            o[0] = index;
            o[1] = adv.getId();
            o[2] = adv.getName();
            dfmLoaiDichVu.addRow(o);
            index++;
        }
        tblLoaiDichVu.setModel(dfmLoaiDichVu);
        funBase.addCheckBox(3, tblLoaiDichVu);

        // Cài đặt sự kiện khi click từng dòng trong bảng
        if (list_LDV.size() > 0) {
            tblLoaiDichVu.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    int currentRow = tblLoaiDichVu.getSelectedRow();
                    if (currentRow < 0) {
                        currentRow = 0;
                    }
                    // Chèn dữ liệu lên form
                    if (currentRow >= 0) {
                        txtMaLoaiDichVu.setText(tblLoaiDichVu.getValueAt(currentRow, 1).toString());
                        txtTenLoaiDichVu.setText(tblLoaiDichVu.getValueAt(currentRow, 2).toString());
                    }
                }
            });
        }
    }

