    private void button_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_deleteMouseClicked
         String succesDeltete = "";
        String errDeltete = "";
        boolean check_del = false;

        int thongBao = JOptionPane.showConfirmDialog(this, "Bạn có chắc không?", "Thông báo!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (thongBao == JOptionPane.YES_OPTION) {

            for (int i = 0; i < tbldichVu.getRowCount(); i++) {
                if (funBase.IsSelected(i, 5, tbldichVu)) {
                    check_del = true;
                    int rowSuccess = dich_vuService.delete(Long.parseLong(tbldichVu.getValueAt(i, 1).toString()));
                    tbldichVu.clearSelection();
                    if (rowSuccess > 0) {
                        succesDeltete += "\t" + tbldichVu.getValueAt(i, 2).toString() + "\n";
                    } else {
                        errDeltete += "\t" + tbldichVu.getValueAt(i, 2).toString() + "\n";
                    }
                }
            }
            loadData();
            ressetText();
            if (check_del == true) {
                String mess = "";
                if (succesDeltete.length() > 0) {
                    mess += "Bạn đã xóa thành công: \n" + succesDeltete;
                }
                if (errDeltete.length() > 0) {
                    mess += "Không thể xóa: \n" + errDeltete;
                }
                JOptionPane.showMessageDialog(rootPane, mess, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn hàng để xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_button_deleteMouseClicked


