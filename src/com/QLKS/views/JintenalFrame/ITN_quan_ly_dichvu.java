    public void loadData() {
        Object[] columnNames = {"STT", "Mã Dịch Vụ", "Tên loại Dịch Vụ", "Giá", "Đơn Vị", ""};
//        if (nameSeaechInput != null) {
//            dfmThietBi = thiet_biService.search(nameSeaechInput);
//        } else {
//            dfmThietBi = cauhinhDAO.getAll();
//        }
        list_dich_vu = dich_vuService.findAll();
        
        dfmDichVu = new DefaultTableModel(new Object[0][0], columnNames);
        int index = 1;
        for (dich_vuModel adv : list_dich_vu) {
            Object[] o = new Object[6];
            o[0] = index;
            o[1] = adv.getId();
            o[2] = adv.getLoai_dich_vuModel().getName();
            o[3] = adv.getPrice();
            o[4] = adv.getDon_viModel().getName_DV();
            dfmDichVu.addRow(o);
            index++;
        }
        tbldichVu.setModel(dfmDichVu);
        funBase.addCheckBox(5, tbldichVu);

        // Cài đặt sự kiện khi click từng dòng trong bảng
        if (list_dich_vu.size() > 0) {
            
            tbldichVu.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    int currentRow = tbldichVu.getSelectedRow();
                    if (currentRow < 0) {
                        currentRow = 0;
                    }

                    // Chèn dữ liệu lên form
                    if (currentRow >= 0) {
                        txt_idDV.setText(tbldichVu.getValueAt(currentRow, 1).toString());
                        txtGiaDichVu.setText(tbldichVu.getValueAt(currentRow, 3).toString());
                    }
                }
            });
        }
    }
