    private void button_deleteTTHDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_deleteTTHDMouseClicked
          String succesDeltete = "";
        String errDeltete = "";
        boolean check_del = false;

        int thongBao = JOptionPane.showConfirmDialog(this, "Bạn có chắc không?", "Thông báo!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (thongBao == JOptionPane.YES_OPTION) {

            for (int i = 0; i < tblTTHD.getRowCount(); i++) {
                if (funBase.IsSelected(i, 3, tblTTHD)) {
                    check_del = true;
                    int rowSuccess = trang_thaiService.delete(Long.parseLong(tblTTHD.getValueAt(i, 1).toString()));
                    tblTTHD.clearSelection();
                    if (rowSuccess > 0) {
                        succesDeltete += "\t" + tblTTHD.getValueAt(i, 2).toString() + "\n";
                    } else {
                        errDeltete += "\t" + tblTTHD.getValueAt(i, 2).toString() + "\n";
                    }
                }
            }
            ressetText();
            loadData();
            if (check_del == true) {
                String mess = "";
                if (succesDeltete.length() > 0) {
                    mess += "Bạn đã xóa thành công: \n" + succesDeltete;

                }
                if (errDeltete.length() > 0) {
                    mess += "Không thể xóa: \n" + errDeltete;
                }
                JOptionPane.showMessageDialog(rootPane, mess, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn hàng để xóa", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_button_deleteTTHDMouseClicked


